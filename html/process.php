    <h1>How Our Maracas are Made</h1>
    
    <ol>
        <li>Wood is harvested from a local Iowan Ironwood Tree Farm</li>
        <li>The harvested wood is transported to our warehouse where it is cut into blocks</li>
        <li>The wood blocks are then carved into the shape of a maraca</li>
        <li>The maracas are then filled with fresh beans after being cut in half, and then glued back together</li>
        <li>After being glued back together, the maracas are hand painted by our professional artists</li>
        <li>The shaped maracas are sanded and shipped off to one of our many store locations</li>
    </ol>
